#Exemplo com Lista
frutas = ["maçã", "banana", "laranja"]
print("Lista de frutas:", frutas)

#Adicionando uma fruta
frutas.append("uva")
print("Lista de frutas atualizadas:", frutas)

#Acessando um item 
print("Primeira fruta:", frutas[3])

#Remover alguma fruta da lista
frutas.remove(frutas[2])
print(frutas)